const START_MS = Date.UTC(2026, 0, 1)

export function getDaily() {
  const date = new Date().toISOString().slice(0, 10)
  const d = new Date(date + 'T00:00:00Z')
  const dayIndex = Math.round((Date.UTC(d.getFullYear(), d.getMonth(), d.getDate()) - START_MS) / 86400000)
  const i = ((dayIndex % SINGLE.length) + SINGLE.length) % SINGLE.length
  const lo = Math.min(MULTI_F1[i], MULTI_F2[i])
  const hi = Math.max(MULTI_F1[i], MULTI_F2[i])
  const oneshot_lo = Math.min(ONE_SHOT_F1[i], ONE_SHOT_F2[i])
  const oneshot_hi = Math.max(ONE_SHOT_F1[i], ONE_SHOT_F2[i])

  return { date, single: SINGLE[i], multi: [lo, hi], oneshot: [oneshot_lo, oneshot_hi] };
}

export function GET(): Response {
  return Response.json(
    getDaily(),
    { headers: { 'Cache-Control': 'public, s-maxage=3600, stale-while-revalidate=86400' } }
  )
}
