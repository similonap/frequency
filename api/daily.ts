const START_MS = Date.UTC(2026, 0, 1)

function getDailyFreq(dateStr: string): number {
  const d = new Date(dateStr + 'T00:00:00Z')
  const dayIndex = Math.round((Date.UTC(d.getFullYear(), d.getMonth(), d.getDate()) - START_MS) / 86400000)
  const i = ((dayIndex % DAILY_FREQS.length) + DAILY_FREQS.length) % DAILY_FREQS.length
  return DAILY_FREQS[i]
}

export function GET(): Response {
  const date = new Date().toISOString().slice(0, 10)
  const freq = getDailyFreq(date)
  return Response.json(
    { date, freq },
    { headers: { 'Cache-Control': 'public, s-maxage=3600, stale-while-revalidate=86400' } }
  )
}
