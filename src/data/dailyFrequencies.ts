export function getDailyFreq(dateStr?: string): number {
  const today = dateStr ?? new Date().toISOString().slice(0, 10)
  const entry = DAILY_FREQS.find(d => d.date === today)
  if (entry) return entry.freq
  // fallback: wrap by day-of-year index for dates outside the list
  const d = new Date(today + 'T00:00:00Z')
  const i = (Math.round((d.getTime() - Date.UTC(2026, 0, 1)) / 86400000) % 365 + 365) % 365
  return DAILY_FREQS[i].freq
}
